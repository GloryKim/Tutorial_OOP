/*
덧셈 뺄셈 표현식 확인하는 프로그램
*/

#include <iostream>
using namespace std;

int main()
{
  // 덧셈 연산자
  cout << "덧셈 연산자" << endl;
  cout << "10 + 5 = " << 10 + 5 << endl;
  cout << "10.5 + 6.2 = " << 10.5 + 6.2 << endl;
  // 뺄셈 연산자
  cout << "뺄셈 연산자" << endl;
  cout << "25 - 30 = " << 25 - 30 << endl;
  cout << "31.2 - 30.4 = " << 31.2 - 30.4 << endl;
  return 0;
}
