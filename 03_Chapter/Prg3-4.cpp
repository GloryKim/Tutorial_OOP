/*
곱셈 *과 나눗셈 / % 파악하기
*/

#include <iostream>
using namespace std;

int main()
{
  // 곱셈
  cout << "* 확인하기" << endl;
  cout << "2 * 3 = " << 2 * 3 << endl;
  cout << "4.4 * 3.1 = " << 4.4 * 3.1 << endl;
  cout << "-31 * 4 = " << -31 * 4 << endl;
  // 나눗셈
  cout << "/ 확인하기" << endl;
  cout << "30 / 5 = " << 30 / 5  << endl;
  cout << "4 / 7 = " << 4 / 7 << endl;
  // 나머지
  cout << "% 확인하기" << endl;
  cout << "3 % 5 = " << 3 % 5 << endl;
  cout << "20 % 4 = " << 20 % 4 << endl;
  cout << "31 % 7 = " << 31 % 7 << endl;
  return 0;
}
